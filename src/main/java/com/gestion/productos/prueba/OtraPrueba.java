package com.gestion.productos.prueba;

public class OtraPrueba {
	
	private int uno;
	private int dos;
	private int tres;
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public int getDos() {
		return dos;
	}
	public void setDos(int dos) {
		this.dos = dos;
	}
	public int getTres() {
		return tres;
	}
	public void setTres(int tres) {
		this.tres = tres;
	}

	@Override
	public String toString() {
		return "OtraPrueba{" +
				"uno=" + uno +
				", dos=" + dos +
				", tres=" + tres +
				'}';
	}
}
